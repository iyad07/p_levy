                    Icons.check_circle,
                    size: 60,
                    color: Colors.green,
                  ),
                ),
              ),
              
              const SizedBox(height: 40),
              
              // Success Message
              SlideTransition(
                position: _slideAnimation,
                child: Column(
                  children: [
                    const Text(
                      'Payment Successful!',
